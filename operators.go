package main

import "fmt"

func main() {
	x := 0
	fmt.Println(x)

	x++            // adds (increments) 1 to x 
	fmt.Println(x)

	x--           // subtract (decrease) 1 from x
	fmt.Println(x)

	x += 42       // adds 42 to x
	fmt.Println(x)

	x -= 42       // subtracts 42 from x 
	fmt.Println(x)

	x *= 42       // multiplys 42 and x 
	fmt.Println(x)




	fmt.Println("something")
}